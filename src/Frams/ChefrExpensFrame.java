    private void SetNew(){
        txtNote.setText("");
        txtamount.setText("0.00");
        Font f = Tools.font(18f);
        titel.setFont(f);
        ConnectDB.fillCombo("typeexpenschefr", "name_typeExpensChefr", comType);
        ConnectDB.fillCombo("suppliers", "name_Suppliers", comSuppler);
        rdCash.setSelected(true);
        Tools.disableButOpen(btPanel);
        
    }
    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
        String St_amount = txtamount.getText();
        double amount = Double.parseDouble(St_amount);
        String nameTypeExpens = comType.getSelectedItem().toString();
        String Supplier = comSuppler.getSelectedItem().toString();
        String note = txtNote.getText();
        boolean isSave = chevor.SaveExpens(chevorletClass.typeCash.Cash, amount, nameTypeExpens, Supplier, note);
        if(isSave){
            Tools.showInfoMsg("تم الحفظ", "حفظ");
            SetNew();
        }
    }//GEN-LAST:event_btsaveActionPerformed

