    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
        double FullAmount = Double.parseDouble(txt_FullAmount.getText());
        double PayAmount = Double.parseDouble(txt_PayAmount.getText());
        String Note = txt_Note.getText();
        String NameSupplier = combSupp.getSelectedItem().toString();
        String nameType = comboType.getSelectedItem().toString();
        MillClass.TypeCash type;
        if(rdCash.isSelected()){
            type = MillClass.TypeCash.Cash;
        }else{
            if(rdDebit.isSelected()){
              type = MillClass.TypeCash.Debit;
            }else{
                type = MillClass.TypeCash.HafCash;
            }
            
        }
        
       boolean isSave =  mill.SaveExpenes(type, PayAmount, FullAmount, nameType, NameSupplier, Note);
       if(isSave){
           Tools.showInfoMsg("تم الحفظ", "حفظ");
           setNew();
       }
    }//GEN-LAST:event_btsaveActionPerformed

