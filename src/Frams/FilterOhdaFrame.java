    private void comOhdaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comOhdaItemStateChanged
        // TODO add your handling code here:
        String nameOhda = comOhda.getSelectedItem().toString();
        if(nameOhda.equals("اختر العهدة")){
            txtBalanceOhda.setText("0.00");
            txtamount.setText("0.00");
            txtamount.setEditable(false);
        }else{
            try {
               String Str_id_trustee = ConnectDB.getIdFrmName("trustee", nameOhda);
               // int id_trustee = Integer.parseInt(Str_id_trustee);
                double balance = ohda.getBalanceOhda(nameOhda);
                txtBalanceOhda.setText(balance+"");
                txtamount.setText(balance+"");
                rdAll.setSelected(true);
                rdSome.setSelected(false);
                txtamount.setEditable(true);
            } catch (SQLException ex) {
                Logger.getLogger(FilterOhdaFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
    }//GEN-LAST:event_comOhdaItemStateChanged
    
