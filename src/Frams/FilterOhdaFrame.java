    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
        try {
            double amount =Double.parseDouble(txtamount.getText());
            double balance = Double.parseDouble(txtBalanceOhda.getText());
            String nameOhda = comOhda.getSelectedItem().toString();
            String idtrustee = ConnectDB.getIdFrmName("trustee", nameOhda);
            int id_trustee = Integer.parseInt(idtrustee);
            int id_ohda = ohda.getIdOhdaFromIdTrus(id_trustee);
            boolean isSave;
            if(rdAll.isSelected()){
                if(amount == balance){
                     isSave = ohda.SaveFilterOhda(amount, nameOhda, id_ohda, nameOhda, true);
                    if(isSave){
                        Tools.showInfoMsg("تم الترحيل", "عهدة " + nameOhda);
                        SetNew();
                    }else{
                        Tools.showErrorMsg("خطأ");
                    }
                }else if(balance > amount){
                    Tools.showErrorMsg("يوجد مبلغ متبقي من العهدة");
                }else{
                    Tools.showErrorMsg("مبلغ التصفية اكبر من العهدة");
                }
                
            }else{
                if(amount < balance){
                    //amount = balance - amount;
                    isSave = ohda.SaveFilterOhda(amount, nameOhda, id_ohda, nameOhda,false );
                    if(isSave){
                        Tools.showInfoMsg("تم تصفية العهدة", "حفظ");
                        SetNew();
                    }
                }
                
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(FilterOhdaFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_btsaveActionPerformed

