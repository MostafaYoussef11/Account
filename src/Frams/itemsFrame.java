    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
        // TODO add your handling code here:
        String low = txtLow.getText();
        String hight = txtHight.getText();
        String id = txtid.getText();
        double val = Double.valueOf(txtVal.getText());
        if(low.equals("")||hight.equals("")){
            Tools.showErrorMsg("البيانا غير كاملة");
        }else{
            i.SetAll(id,low, hight, val);
            if(i.Save()){
                Tools.showInfoMsg("تم الحفظ بنجاح", "حفظ");
                txtHight.setText("");
                txtLow.setText("");
                txtVal.setText("");
                i.filTable(jTable1);
            }
            else{
                Tools.showErrorMsg("خطأ في الحفظ");
            }
        }
        
    }//GEN-LAST:event_btsaveActionPerformed

