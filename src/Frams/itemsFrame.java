    private void btsave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsave1ActionPerformed
        // TODO add your handling code here:
        String iditems = txtidItem.getText();
        String name = txtname.getText();
        String high = combo.getSelectedItem().toString();
        int firstbalance = Integer.valueOf(txtFirstBalance.getText());
        if(name.equals("")){
            Tools.showErrorMsg("اسم الصنف غير مكتوب");
        }else{
            i.setItemData(iditems, name, high, firstbalance);
            if(i.saveItems()){
                Tools.showInfoMsg("تم الخفظ بمجاح", "حفظ");
                txtFirstBalance.setText("");
                txtidItem.setText(i.lastiditem());
                txtname.setText("");
                i.fillTableItems(jTable2);
            }else{
                Tools.showErrorMsg("خطأ في حفظ الصنف");
            }
        }
    }//GEN-LAST:event_btsave1ActionPerformed

