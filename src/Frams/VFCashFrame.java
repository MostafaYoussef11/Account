    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
       //set data 
      String number = txtNumber.getText();
      String id_interntional = txtIdInternational.getText();
      String name = txtname.getText();
      String sim = txtSim.getText();
      String balance_st = txtBalance.getText();
      double balance = 0;
      try{
          balance = Double.parseDouble(balance_st);
      }catch(NumberFormatException ex){
          Logger.getLogger(getClass().getName()).log(Level.SEVERE, null, ex);
      }
      vf.setBalance(balance);
      vf.setId_international(id_interntional);
      vf.setName_owner(name);
      vf.setNumber_VF_cash(number);
      vf.setSim(sim);
      boolean isSave = vf.Save();
      if(isSave){
          Tools.showInfoMsg("تم الحفظ بنجاح", "حفظ");
          newVfCash();
      }
    }//GEN-LAST:event_btsaveActionPerformed

