    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
      WalltNumber = comWallt.getSelectedItem().toString();
      amount = Double.parseDouble(txtAmount.getText());
      value = Double.parseDouble(txtValue.getText());
      boolean isSave = trans.SaveWithdrawATM(WalltNumber, amount, value);
      if(isSave){
          Tools.showInfoMsg("تم الحفظ بنجاح", "حفظ");
          SetNew();
      }else{
          Tools.showErrorMsg("خطأ");
      }
    }//GEN-LAST:event_btsaveActionPerformed

