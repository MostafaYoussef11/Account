    private void checkWallt(String WalltNumber){
        id_wallt = vf.getId_VF_ByNumber(WalltNumber);
        amount = 0.00;
        value = 0.00;
        txtAmount.setText(amount+"");
        txtValue.setText(value+"");
        SumWithdraw = vf.getLamiteOfMonth_SendAndWithdraw(id_wallt);
        txtSumWithdraw.setText(SumWithdraw+"");
        balance = vf.getNowBalance(WalltNumber);
        txtBalance.setText(balance+"");
        Residual = 100000 - SumWithdraw;
        txtResidual.setText(Residual+"");
    
    }
