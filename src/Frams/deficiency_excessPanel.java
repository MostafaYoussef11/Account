    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean isSaved = false;
        String amount_st = txt_amount.getText();
        double amount = Double.parseDouble(amount_st);
        if(amount_st.length() == 0 || amount == 0){
            Tools.showErrorMsg("المبلغ غير مكتوب");
        }else{
            deficiency_excess def_ex = new deficiency_excess();
           
            if(rd_deficiency.isSelected()){
                try {
                     def_ex.setData(amount, "عجز");
                    isSaved = def_ex.SaveDeficiency();
                } catch (SQLException ex) {
                    Logger.getLogger(deficiency_excessPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                try {
                     def_ex.setData(amount, "زيادة");
                    isSaved = def_ex.SaveExcess();
                } catch (SQLException ex) {
                    Logger.getLogger(deficiency_excessPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            Tools.showInfoMsg("تم الحفظ", "حفظ");
            setNewDeficExc();
        }
        //if(){}
    }//GEN-LAST:event_jButton1ActionPerformed


