    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
        Date datePay = txtDateReturn.getDate();
        String St_amount = txtAmount.getText();
        double amount = Double.parseDouble(St_amount);
        String name = txtNameSub.getText();
        String note = txtNote.getText();
        solfa.SetDateSolfa(datePay, amount, name, note);
        boolean isSave = solfa.Save();
        if(isSave){
            Tools.showInfoMsg("تم الحفظ بنجاح", "حفظ");
            SetNewSolfa();
        }else{
            Tools.showErrorMsg("خطأ");
        }   
    }//GEN-LAST:event_btsaveActionPerformed

