    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
        String name_trustee = comName.getSelectedItem().toString();
        int id_trustee = ohda.getIdTrustee(name_trustee);
        String StrAmount = txt_amount.getText();
        double amount = Double.parseDouble(StrAmount);
        String note = txt_note.getText();
        Date date = new Date();
        boolean isSave = ohda.SaveOhda(date, amount, id_trustee, note);
        if(isSave){
            Tools.showInfoMsg("تم الحفظ", "حفظ");
            SetNew();
        }else{
            Tools.showErrorMsg("خطأ");
            SetNew();
        }
    }//GEN-LAST:event_btsaveActionPerformed

