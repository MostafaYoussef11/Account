    private void comTypeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comTypeItemStateChanged
        // TODO add your handling code here:
        String type = comType.getSelectedItem().toString();
        if(type.equals("داخلي")){
            comWork.setEnabled(true);
        }else{
            comWork.setEnabled(false);
        }
    }//GEN-LAST:event_comTypeItemStateChanged

    private void comAmountItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comAmountItemStateChanged
        // TODO add your handling code here:
        String amountType = comAmount.getSelectedItem().toString();
        if(amountType.equals("نقدي")){
             comClient.setEnabled(false);
        }else{
            comClient.setEnabled(true);
        }
    }//GEN-LAST:event_comAmountItemStateChanged

